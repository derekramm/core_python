"""ex01_大小写转换.py"""

# s.upper()：全部大写
# s.lower()：全部小写
# s.title()：每个单词的首字母大写
# s.capitalize()：首字母大写
# s.swapcase()：交换大小写字母

s = 'xiao Ming'

print(s.upper())  # XIAO MING
print(s.lower())  # xiao ming
print(s.title())  # Xiao Ming
print(s.capitalize())  # Xiao ming
print(s.swapcase())  # XIAO mING
