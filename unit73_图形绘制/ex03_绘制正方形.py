# -*- coding:utf-8 -*-
"""ex03_绘制正方形.py"""

import turtle

turtle.color('red', 'yellow')  # 画笔颜色
turtle.pensize(20)  # 画笔大小
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.done()  # 让屏幕一直显示
